const mongoose = require('mongoose');

const addressSchema = new mongoose.Schema({
  company_name: {
    type: String,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  zip: {
    type: String,
    required: true,
  },
  street: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  fax: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model('Address', addressSchema);
